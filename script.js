    shareButton.textContent = 'üì∏ –°–æ–∑–¥–∞—ë–º –ø–ª–∞–∫–∞—Ç...';
    shareButton.disabled = true;

    // –°–æ–∑–¥–∞–µ–º —Å–∫—Ä–∏–Ω—à–æ—Ç –í–°–ï–ô —Å—Ç—Ä–∞–Ω–∏—Ü—ã
