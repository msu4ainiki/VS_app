    setupEventListeners();
    loadInitialImages();
    
    const shareButton = document.getElementById('create-poster');
    if (shareButton) {
        shareButton.addEventListener('click', createAndSharePoster);
        shareButton.textContent = 'üì∏ –°–æ–∑–¥–∞—Ç—å –ø–ª–∞–∫–∞—Ç';
    }
