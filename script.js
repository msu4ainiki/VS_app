        scale: 0.8, // –£–º–µ–Ω—å—à–∞–µ–º –º–∞—Å—à—Ç–∞–± –¥–ª—è –æ–ø—Ç–∏–º–∞–ª—å–Ω–æ–≥–æ —Ä–∞–∑–º–µ—Ä–∞ —Ñ–∞–π–ª–∞
